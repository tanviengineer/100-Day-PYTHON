# Break Statement : (stop the loop) it enables program to skip over a part of the code. A break statement terminates the very loop it lies within.
for i in range(12):
    print("5 * ",i," = ",5*i)
    if (i==10):
        break

# Countinoues Statement :  (skip the iteration ) it skips the rest of the loop statement and causes the next iteration to occur.
for i in range(17):
    print("10 * ",i," = ",10*i)
    if i==10:
        continue 

